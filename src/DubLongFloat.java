public class DubLongFloat {
    public static void main(String[] args) {

        int a = 6;
        long b = 9990909090990909L;
        float c = 3.145F;
        double d = 9.303049494;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
