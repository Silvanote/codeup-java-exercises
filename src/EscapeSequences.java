public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Dog");
        System.out.print("Cat ");
        System.out.println("Bird");
        System.out.println("ABC 123");
        System.out.println("The \"fish\" was blue.");
        System.out.println("These are double qoutes\"\".");
        System.out.println("This is a backslash \\");

        System.out.println("What do you mean \"stinky\"?");
        System.out.println("The file is located at ~ \\Desktop\\Codeup\\Java");
        System.out.println("This is a tab \t!");
        System.out.println("This \n is a \n on \n multiple times \n");
    }
}
